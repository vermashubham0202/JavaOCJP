package com.pidkui.aa_language_fundamentals.a_identifiers;

/*
Name in Java program is considered as identifier which can be used for identification purpose.
It can be a class name, method name or variable name or label name also.
 */

public class IdentifierDemo01 {
    public static void main(String[] args) {
        int x = 10;
    }
}

/*
Total identifiers in this program is: 5

Identifiers: IdentifierDemo01, main, String (String is pre-defined java class name), args, x
 */